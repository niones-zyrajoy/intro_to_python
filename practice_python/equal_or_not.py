"""
enter two numbers and checks if they are equal.
If the numbers are equal, it prints a message indicating that.
Otherwise, it prints a message indicating that the numbers are not equal.
"""
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
if num1 == num2:
    print("The numbers are equal.")
else:
    print("The numbers are not equal.")
